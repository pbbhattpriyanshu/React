// Why we use React 
// React is used because it makes building interactive user interfaces easier and more efficient. Here are some key reasons:

// Component-Based: You can break your UI into reusable components.
// Declarative: You describe what you want to see, and React updates the UI when your data changes.
// Efficient Updates: React uses a virtual DOM to update only the parts of the page that change, making updates fast.
// Strong Ecosystem: There are many libraries and tools built around React.
// Large Community: Lots of resources, tutorials, and support are available.